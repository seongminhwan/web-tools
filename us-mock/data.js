// 辅助函数
function getRandomElement(array) {
    return array[Math.floor(Math.random() * array.length)];
}

function getRandomNumber(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
}

// 生成随机街道地址
function generateStreetAddress() {
    const streetNumber = getRandomNumber(1, 9999);
    const streetName = getRandomElement(STREET_NAMES);
    const streetType = getRandomElement(STREET_TYPES);

    return `${streetNumber} ${streetName} ${streetType}`;
}

// 生成随机公寓号（30%概率）
function generateApartment() {
    if (Math.random() < 0.3) {
        const aptType = getRandomElement(APARTMENT_TYPES);
        const aptNumber = aptType === 'Floor' ? getRandomNumber(1, 20) :
            aptType === '#' ? getRandomNumber(1, 999) :
                getRandomNumber(1, 999);
        return `${aptType} ${aptNumber}`;
    }
    return '';
}

// 生成随机生日
function generateBirthDate(ageRange = '18-80') {
    const [minAge, maxAge] = ageRange.split('-').map(Number);
    const currentYear = new Date().getFullYear();
    const birthYear = currentYear - getRandomNumber(minAge, maxAge);
    const birthMonth = getRandomNumber(1, 12);
    const birthDay = getRandomNumber(1, 28); // 使用28避免月份天数问题

    return {
        year: birthYear,
        month: birthMonth,
        day: birthDay
    };
}

// 生成随机姓名（支持多种风格）
function generateName(gender, style = 'mixed') {
    const firstNames = gender === 'Male' ? FIRST_NAMES_MALE : FIRST_NAMES_FEMALE;
    const lastName = getRandomElement(LAST_NAMES);

    let firstName;

    switch (style) {
        case 'simple':
            // 只选择单个单词的名字
            firstName = getRandomElement(firstNames.filter(name => !name.includes(' ') && !name.includes('-')));
            break;
        case 'compound':
            // 优先选择多单词的名字
            const compoundNames = firstNames.filter(name => name.includes(' ') || name.includes('-'));
            firstName = compoundNames.length > 0 ? getRandomElement(compoundNames) : getRandomElement(firstNames);
            break;
        case 'mixed':
        default:
            // 随机选择
            firstName = getRandomElement(firstNames);
            break;
    }

    return {
        firstName,
        lastName
    };
}

